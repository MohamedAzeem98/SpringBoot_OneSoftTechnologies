package com.example.UseProductDetails;
public class ProductEntity {
	private int id;
	private String name;
	private int hnt;
	private int price;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getHnt() {
		return hnt;
	}
	public void setHnt(int hnt) {
		this.hnt = hnt;
	}
	public void setPrice(int price) {
		this.price=price;
	}
	public int getPrice() {
		return price;
	}
	

}
